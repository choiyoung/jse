package cmm04.arry;

public class No04_MultiDimarrayDemo {

	public static void main(String[] args) {
		String[][] name = {
				{"Mr","Mrs","Ms"},
				{"Smith","John"}	
		};
		System.out.println(name[0][0]+"."+name[1][0]);
		
		System.out.println(name[0][2]+"."+name[1][1]);
		
		/*
		 Mr.smith
		 Ms.John 을 출력 시키세여
		  */
	}

}
